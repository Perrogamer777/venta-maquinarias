#!/bin/bash

# Script para rebuild completo con variables de entorno
set -e

PROJECT_ID="silken-fortress-479315-b0"
REGION="us-central1"
SERVICE_NAME="dashboard-cipres"

echo "üîê Leyendo variables de .env.local..."
source .env.local

echo "üèóÔ∏è  Haciendo rebuild completo con variables de entorno..."
